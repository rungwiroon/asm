	SHOWMENU	PROC		;MENU
	
	;PUSHA
	
	MOV	BMP_X,0
	MOV	BMP_Y,0
	PASTEBMP	0,0,MENUPLAY

	
	;JMP LOPP
	CALL	CLEAR_KEY_BUFF
MENU_LOOP:
	
	;CALL TIME_INT
	
	MOV	AH,01			;WAITING FOR KEY
	INT	16H
	JZ	MENU_LOOP
	
	MOV	AH,0
	INT	16H
	
	CMP	AH,72			;CHECK KEY UP ARROW
	JE	UP_ARROW
	CMP	AH,80			;CHECK KEY DOWN ARROW
	JE	DOWN_ARROW
	CMP	AL,13			;CHECK KEY ENTER
	JE	ENTER_MENU
	JMP	MENU_LOOP		; LOOP MENU_LOOP
	
UP_ARROW:				;LOOP	UP ARROW
	DEC	CHOICE			;DECREASE CHOICE
	CMP	CHOICE,1		;CHECK CHOICE
	JLE	MENU_PLAY
	CMP	CHOICE,2
	JE	MENU_HIGH
	CMP	CHOICE,3
	JE	MENU_HOW
	CMP	CHOICE,4
	JE	MENU_EXIT
	
DOWN_ARROW:				;LOOP DOWN ARROW
	INC	CHOICE			;INCREASE CHOICE
	CMP	CHOICE,1		;CHECK CHOICE
	JE	MENU_PLAY
	CMP	CHOICE,2
	JE	MENU_HIGH
	CMP	CHOICE,3
	JE	MENU_HOW
	CMP	CHOICE,4
	JGE	MENU_EXIT
	
MENU_PLAY:
	CMP	CHOICE,1		;CHOICE = 1 DISPLAY MENUPLAY
	JL	CHOICE_LESS
	PASTEBMP	0,0,MENUPLAY
	JMP	MENU_LOOP
	
CHOICE_LESS:
	MOV	CHOICE,4
	
MENU_HIGH:
	CMP	CHOICE,2		;CHOICE = 2 DISPLAY MENUHIGH
	JNE	MENU_HOW
	PASTEBMP	0,0,MENUHIGH
	JMP	MENU_LOOP	
	
MENU_HOW:
	CMP	CHOICE,3		;CHOICE = 3 DISPLAY MENUHOW
	JNE	MENU_EXIT
	PASTEBMP	0,0,MENUHOW
	JMP	MENU_LOOP
	
MENU_EXIT:
	CMP	CHOICE,4
	JG	CHOICE_MORE
	PASTEBMP	0,0,MENUEXIT
	JMP	MENU_LOOP
	
CHOICE_MORE:
	MOV	CHOICE,1
	JMP	MENU_PLAY
	
ENTER_MENU:		;ENTER FOR RETURN
	;POPA
	RET

	SHOWMENU	ENDP
;=========================================================================================================================	