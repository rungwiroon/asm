POS	PROC
	PUSH	AX
	PUSH	BX
	PUSH	CX
	PUSH	DX
	
;GO
	GOTOXY	0,0
	WRITE_CHAR	'X',2
	WRITE_CHAR	':',2
	GOTOXY	0,1
	WRITE_CHAR	'Y',2
	WRITE_CHAR	':',2
RDMSE:
	MOV	AX,CX
	GOTOXY	2,0
	CALL	DISP
    MOV	AX,DX
    GOTOXY	2,1
    CALL	DISP

;EXIT   
	POP DX
	POP CX
	POP	BX
	POP AX
	RET     	
POS	ENDP
	
DISP	PROC
	PUSH	CX
	
	MOV	CX,4
NYBL:
	ROL	AX,1
	ROL	AX,1
	ROL	AX,1
	ROL	AX,1
	PUSH	AX
	AND	AL,0FH
	ADD AL,30H
	CMP	AL,3AH
	JB	NO10
	ADD	AL,7
NO10:	
	WRITE_CHAR	AL,2
	POP	AX
	ADD	DI,2
	LOOP	NYBL
	POP	CX

	RET						;RETURN
DISP	ENDP