MAIN	PROC

	MOV	AX,@DATA	
	MOV	DS,AX		;INITAILZE DATA SEGMENT
	MOV	ES,AX		;INITALIZE EXTRA SEGMENT

	MOV	AX,13H
	INT	10H		;SET DISPLAY MODE 13H

	SET_TIMER_VEC		;MACRO SET TIMER VECTOR
	SET_KEY_VEC		;MACRO SET KEYBOARD VECTOR

	CALL	RANDOMIZE	;GENERATE SEED FOR RANDOM

	MOV	AX,1001H
	MOV	BH,250
	INT	10H		;SET BORDER COLOR
	
;===============================
;CALL	 TO MAINMENU PROCEDURE
;===============================
START:

	CALL	MAINMENU	;CALL GAME MAIN MENU
		
;==============
;EXIT GAME
;==============
@EXIT:
	NBMP	0,0,BYE		;BYE BYE PICTURE
	DELAY	30		;DELAY SHOW PICTURE
	CALL	FADE		;FADE SCREEN
		
	RESET_KEY_VEC		;MACRO RESET KEYBOARD VECTOR
	RESET_TIMER_VEC		;MACRO RESET TIMER VECTOR
	
	MOV	AX,0003H	;RESET TO TEXT MODE
	INT	10H			

	MOV	AX,4C00H	;RETURN TO DOS	
	INT	21H

MAIN	ENDP


;===============================================
;SHOW MAIN MENU
;INPUT : KEYBOARD TO SELECT MENU
;===============================================
MAINMENU	PROC
	MOV	MENU_CHOICE,'1'
MM_1:
	NBMP	0,0,BG1			;BACKGROUND MAIN MENU
	MOV	AL,MENU_CHOICE		;DEFINE CHOICE
	MOV	BG1_MENU[4],AL	
	FBMP	229,67,BG1_MENU		;DISPLAY SELECT MENU
	
	GOTOXY	0,24			;SET POSITION TO SHOW TIME
	MOV	CLOUD_X,START_POS_X	
	MOV	CLOUD_Y,START_POS_Y
	
MM_CLOUD:
	CALL	TIME_INT		;UPDATE TIME
	MOV	CAN_MOVE_CLOUD,1	;SET FLAG TO MOVE CLOUD
	CALL	MOVE_CLOUD_X		;MOVE CLOUD IN HORIZONTAL
	CMP	KEY_FLAG,1		
	JNE	MM_CLOUD
	MOV	KEY_FLAG,0
	
MM_CK1:
	CMP	SCAN_CODE,UP_ARROW
	JNE	MM_CK2
	
	MOV	MENU_CHOICE,'1'
	MOV	AL,MENU_CHOICE
	MOV	BG1_MENU[4],AL
	FBMP	229,67,BG1_MENU
	JMP	MM_CLOUD

MM_CK2:
	CMP	SCAN_CODE,DOWN_ARROW
	JNE	MM_CK3
	
	MOV	MENU_CHOICE,'2'
	MOV	AL,MENU_CHOICE
	MOV	BG1_MENU[4],AL
	FBMP	229,67,BG1_MENU
	JMP	MM_CLOUD
	
MM_CK3:	
	CMP	SCAN_CODE,ESC_KEY	;CHECK ESC KEY
	JNE	MM_CK4	
	RET				;EXIT GAME	
	
MM_CK4:
	CMP	SCAN_CODE,ENTER_KEY	;ENTER KEY
	JNE	MM_CLOUD
	
	CMP	MENU_CHOICE,'1'		;CHECK CHOICE
	JG	MM_HISCORE
	
	;CALL	FADE			;FADE SCREEN
	CALL	LEVEL1			;PLAYGAME

	RESET_KEY_VEC
	CALL    GET_NAME		;GET NAME FOR END LEVEL
	SET_KEY_VEC
	
MM_HISCORE:
	;CALL	FADE			;FADE SCREEN
	CALL	HIGH_SCORE		;SHOW HI SCORE
	JMP	MM_1
	
MAINMENU	ENDP

