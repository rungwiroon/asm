LEVEL1	PROC
;INTIAL MAP
	LEA	SI,MAP1			;SOURCE --->MAP LEVEL 1
	LEA	DI,MAP			;DESTINATION ---> MAP
	CLD				;CLEAR DIRECTION FLAG
	MOV	CX,91			;COPY 91 LOOP
	REP	MOVSW			;COPY ARRAY MAP1 TO MAP

;INITAIL POSITION
	MOV	COUNT_CUT,90		;FOR CHECK CUT COMPLETE
	MOV	WALK_X,1
	MOV	WALK_Y,1		;DEFINE FIRST POSITION
	MOV	FUEL,'9'
	FBMP	0,0,BG_LV1
	
	MOV	SCORE,0			;CLEAR SCORE
	CALL	SHOW_SCORE		
	
	MOV	POS_X,21
	MOV	POS_Y,9
	TBMP	POS_X,POS_Y,0,MAN1

;INITAIL CLOUD	
	MOV	COUNT_DELAY,180		;TIME FOR WAIT CLOUD
	MOV	CAN_MOVE_CLOUD,0	;START WITH NO CLOUD
	MOV	CLOUD_Y,0		;START CLOUD POSITION
	MOV	COUNT_DELAY2,200	;TIME FOR DECREMENT FUEL

LV1_MC:					;LOOP PLAY GAME	
	CMP	COUNT_CUT,0
	JE	LV1_END
	
	CMP	TIMER_FLAG,1		;CHECK TIMER TICK
	JNE	LV1_CK			;NOT TICK,
	MOV	TIMER_FLAG,0		
	
	CALL	MOVE_CLOUD_Y
	
	DEC	COUNT_DELAY
	JNZ	LV1_FUEL
	MOV	COUNT_DELAY,180
	MOV	CAN_MOVE_CLOUD,1
	CALL	RANDOM_CLOUD

LV1_FUEL:	
	DEC	COUNT_DELAY2
	CMP	COUNT_DELAY2,0
	JG	LV1_CK
	MOV	COUNT_DELAY2,200
	DEC	FUEL
	CALL	SHOW_FUEL
	
	CMP	FUEL,'0'
	JG	LV1_CK
	RET

LV1_CK:
	CMP	KEY_FLAG,1		;CHECK KEY
	JNE	LV1_MC
	MOV	KEY_FLAG,0
	
	CALL	PLAYGAME
	
LV1_ESC:
	CMP	SCAN_CODE,ESC_KEY
	JNE	LV1_MC
	RET

LV1_END:	
	CALL	END_LEVEL 
	CALL	LEVEL2
	RET
	
LEVEL1	ENDP

