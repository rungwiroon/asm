LV5_ESC:
	CMP	SCAN_CODE,ESC_KEY
	JNE	LV5_MC
	RET

LV5_END:	
	CALL	END_LEVEL 
	RET
	
LEVEL5	ENDP

;=============================================
;PLAY GAME
;INPUT : KEYBOARD TO MOVE OBJECT
;OUTPUT : MOVEMENT OBJECT ON SCREEN
;=============================================
PLAYGAME	PROC

PG_1:
	CMP	SCAN_CODE,UP_ARROW
	JNE	PG_2
	CALL	WALK_UP
	SUB	COUNT_DELAY2,5
	RET

PG_2:
	CMP	SCAN_CODE,DOWN_ARROW
	JNE	PG_3
	CALL	WALK_DOWN
	SUB	COUNT_DELAY2,5
	RET
		
PG_3:
	CMP	SCAN_CODE,RIGHT_ARROW
	JNE	PG_4
	CALL	WALK_RIGHT
	SUB	COUNT_DELAY2,5
	RET
	
PG_4:
	CMP	SCAN_CODE,LEFT_ARROW
	JNE	NOT_ARROW
	CALL	WALK_LEFT
	SUB	COUNT_DELAY2,5

NOT_ARROW:	
	RET

PLAYGAME	ENDP

;=========================================================
;CHECK CAN WALK THROUGH ?
;=========================================================
WALK_UP	PROC
	DEC	WALK_Y		;DEC Y POSITION
	CALL	CHK_WALK	;CHECK CAN WALK?
	CMP	WALK,1		;IF CAN WALK
	JE	WU_1		;JMP TO MOVE MAN
	INC	WALK_Y		;IF CAN'T WALK INC Y POSITION
	RET
WU_1:
	SET_ARY	WALK_X,WALK_Y,2,MAP
	RE_SCR	0,0,238,195
	FBMP	POS_X,POS_Y,CUT
	SUB	POS_Y,BLOCK_HEIGHT
	TBMP	POS_X,POS_Y,0,MAN1
	MOV	DIRECTION,UP
	RET

WALK_UP	ENDP

WALK_DOWN	PROC
	INC	WALK_Y
	CALL	CHK_WALK
	CMP	WALK,1
	JE	WD_1
	DEC	WALK_Y
	RET
WD_1:
	SET_ARY	WALK_X,WALK_Y,2,MAP
	RE_SCR	0,0,238,195
	FBMP	POS_X,POS_Y,CUT
	;CMP	DIRECTION,DOWN
	;JE	NORM_DW
	ADD	POS_Y,10
	TBMP	POS_X,POS_Y,0,MAN2
	ADD	POS_Y,6
	
	RET
WALK_DOWN	ENDP

WALK_RIGHT	PROC
	INC	WALK_X
	CALL	CHK_WALK
	CMP	WALK,1
	JE	WR_1
	DEC	WALK_X
	RET
WR_1:
	SET_ARY	WALK_X,WALK_Y,2,MAP
	RE_SCR	0,0,238,195
	FBMP	POS_X,POS_Y,CUT
			
	ADD	POS_X,10
	TBMP	POS_X,POS_Y,0,MAN3
	ADD	POS_X,7

	RET
WALK_RIGHT	ENDP

WALK_LEFT	PROC
	DEC	WALK_X
	CALL	CHK_WALK
	CMP	WALK,1
	JE	WL_1
	INC	WALK_X
	RET
	
WL_1:
	SET_ARY	WALK_X,WALK_Y,2,MAP
	RE_SCR	0,0,238,195
	FBMP	POS_X,POS_Y,CUT
	SUB	POS_X,BLOCK_WIDTH
	TBMP	POS_X,POS_Y,0,MAN4

	RET
WALK_LEFT	ENDP

CHK_WALK	PROC
	GET_ARY	WALK_X,WALK_Y,MAP
	CMP	ARY_VAL,0
	JE	WALK_FALSE

WALK_TRUE:	
	MOV	WALK,1		;CAN WALK
	
	CMP	ARY_VAL,1	;WALK FIRST TIME?
	JE	CW_UPDATE_SCORE
	RET
	
CW_UPDATE_SCORE:
	ADD	SCORE,5
	CALL	SHOW_SCORE
	DEC	COUNT_CUT
	RET

WALK_FALSE:
	MOV	WALK,0		;CAN'T WALK
	RET

CHK_WALK	ENDP

