MAIN	PROC

	MOV	AX,@DATA
	MOV	DS,AX
	MOV	ES,AX
;set graphic mode
	MOV	AX,13H
	INT 10H
;set up timer interrupt vector	
	MOV NEW_TIMER_VEC,OFFSET TIMER_TICK ;offset
    MOV NEW_TIMER_VEC+2,CS		 ;segment
    MOV	AL,1CH				 ;interrupô type
 	LEA	DI,OLD_TIMER_VEC	;DI points to vector buffer
	LEA	SI,NEW_TIMER_VEC	;SI points to new vector
	CALL	SETUP_INT
;set up keyboard interrupt vector
	MOV	NEW_KEY_VEC,OFFSET KEYBOARD_INT
	MOV	NEW_KEY_VEC+2,CS
	MOV	AL,9H
	LEA	DI,OLD_KEY_VEC
	LEA	SI,NEW_KEY_VEC
	CALL	SETUP_INT
;==================================================================
;CALL	 TO MAINMENU PROCEDURE
;==================================================================
START:
	PASTEBMP 0,0,BG1		;SET COLOR TABLE
	CALL	SAVESCREEN
	CALL	MAINMENU
		
;==================================================================
;Exit game
;==================================================================
@EXIT:
	CALL	RESTORESCREEN
	TPASTEBMP 0,0,BYE
	DELAY	36
	
;-Reset timer interrupt vector
	LEA	DI,NEW_TIMER_VEC
	LEA	SI,OLD_TIMER_VEC
	MOV	AL,1CH
	CALL	SETUP_INT
;-reset keyboard interrupt vectror
	LEA	DI,NEW_KEY_VEC
	LEA	SI,OLD_KEY_VEC
	MOV	AL,9H
	CALL	SETUP_INT
;Reset to text mode	
	mov	ax,0003h
	int	10h			
;Return to dos
	mov	ax,4c00h	
	int	21h
MAIN	ENDP

;===============================================
;SHOW MAIN MENU
;INPUT : KEYBOARD TO SELECT MENU
;===============================================
MAINMENU	PROC
TK:	
	FPASTEBMP 0,0,BG1
TK_0:
	;FPASTEBMP 0,0,BG1
	;DELAY	10
	;FPASTEBMP 0,0,BG2
	;DELAY	10
	CMP	KEY_FLAG,1
	JNE	TK_0
	MOV	KEY_FLAG,0
	CMP	SCAN_CODE,ESC_KEY
	JNE TK_1
	JMP	@EXIT
TK_1:
	CMP	SCAN_CODE,ENTER_KEY
	JNE	TK_0
	CALL	PLAYGAME
	CALL	SAVESCREEN
	JMP TK
	RET
MAINMENU	ENDP

;=============================================
;PLAY GAME
;INPUT : KEYBOARD TO MOVE OBJECT
;OUTPUT : MOVEMENT OBJECT ON SCREEN
;=============================================
PLAYGAME	PROC

PG:
	MOV	WALK_X,1
	MOV	WALK_Y,1
	FPASTEBMP 0,0,LEVEL1
	CALL	SAVESCREEN
	;DELAY	18
	
	MOV	POS_X,18
	MOV	POS_Y,8
	FPASTEBMP	POS_X,POS_Y,MAN1
	MOV	DIRECTION,1
	
;CHECK KEY FOR PLAY
PG_0:
	CMP	KEY_FLAG,1
	JNE	PG_0
	MOV	KEY_FLAG,0

PG_1:
	CMP	SCAN_CODE,UP_ARROW
	JNE	PG_2
	CALL	WALK_UP
	JMP	PG_0

PG_2:
	CMP	SCAN_CODE,DOWN_ARROW
	JNE PG_3
	CALL	WALK_DOWN
	JMP	PG_0
		
PG_3:
	CMP	SCAN_CODE,RIGHT_ARROW
	JNE PG_4
	CALL	WALK_RIGHT
	JMP	PG_0
	
PG_4:
	CMP	SCAN_CODE,LEFT_ARROW
	JNE PG_5
	CALL	WALK_LEFT
	JMP	PG_0
	
PG_5:
	CMP	SCAN_CODE,ENTER_KEY
	JNE	PG_6
	JMP	PG

PG_6:	
	CMP	SCAN_CODE,ESC_KEY
	JE	PLAY_END
	JMP	PG_0

PLAY_END:	
	RET

PLAYGAME	ENDP

;=========================================================
;CHECK CAN WALK THROUGH ?
;=========================================================
WALK_UP	PROC
	DEC	WALK_Y
	CALL	CHECK_WALK
	CMP	WALK,1
	JE	WU_1
	INC	WALK_Y
	RET
WU_1:
	CALL	RESTORESCREEN
	CALL	MOVEMAN_UP
	MOV	DIRECTION,1
	RET

WALK_UP	ENDP

WALK_DOWN	PROC
	INC WALK_Y
	CALL	CHECK_WALK
	CMP	WALK,1
	JE	WD_1
	DEC WALK_Y
	RET
WD_1:	
	CALL	RESTORESCREEN
	CALL	MOVEMAN_DOWN
	MOV	DIRECTION,2
	RET
WALK_DOWN	ENDP

WALK_RIGHT	PROC
	INC	WALK_X
	CALL	CHECK_WALK
	CMP	WALK,1
	JE	WR_1
	DEC	WALK_X
	RET
WR_1:	
	CALL	RESTORESCREEN
	CALL	MOVEMAN_RIGHT
	MOV	DIRECTION,3
	RET
WALK_RIGHT	ENDP

WALK_LEFT	PROC
	DEC	WALK_X
	CALL	CHECK_WALK
	CMP	WALK,1
	JE	WL_1
	INC	WALK_X
	RET
WL_1:	
	CALL	RESTORESCREEN
	CALL	MOVEMAN_LEFT
	MOV	DIRECTION,4
	RET
WALK_LEFT	ENDP

CHECK_WALK	PROC
	PUSH	BX
	PUSH	AX
	
	XOR	BX,BX
	MOV	BL,WALK_X
	MOV	AL,WALK_Y
	MOV	AH,14
	MUL	AH	
	ADD	BL,AL
		
	MOV	AL,MAP[BX]
	CMP	AL,1
	JNE	WALK_FALSE
WALK_TRUE:	
	MOV	WALK,1
	JMP	CW1
	
WALK_FALSE:
	MOV	WALK,0
	
CW1:
	POP	AX
	POP	BX
	RET
	
CHECK_WALK	ENDP	

