RANDOM_CLOUD	PROC

	CALL	RANDOM		;RANDOM NUMBER
	MOV	AX,RAND_NUM	
	MOV	BX,12			;FOR DIVIDER
	XOR	DX,DX			;CLEAR REMAINDER
	DIV	BX				;FOR 0-11
	
	MOV	GRASS_COL,DL
	
	MOV	AX,DX			;FOR MULTIPLY
	MOV	BX,BLOCK_WIDTH	;BY X START POSITION
	MUL	BX
	ADD	AX,START_POS_X
	MOV	CLOUD_X,AX
		
	
	CALL	RANDOM
	MOV	AX,RAND_NUM
	MOV	BX,11
	XOR	DX,DX
	DIV BX				;FOR 0-10
	
	MOV	GRASS_ROW,DL
	
	MOV	AX,DX
	MOV	BX,BLOCK_HEIGHT
	MUL	BX
	ADD	AX,START_POS_Y
	;MOV	CLOUD_Y,AX
	MOV	CLEAR_Y,AX
	
	RET
	
RANDOM_CLOUD	ENDP


;====================================================
;check for clear grass and set array
;====================================================
CLEAR_CUT	PROC
	INC GRASS_COL	;for check array
	INC GRASS_ROW
	GET_ARRAY GRASS_COL,GRASS_ROW,MAP
	CMP	ARY_VAL,2
	JE	CLEAR_CUT1
	RET
	
CLEAR_CUT1:
	SET_ARRAY GRASS_COL,GRASS_ROW,1,MAP
	FPASTEBMP	CLOUD_X,CLEAR_Y,GRASS
	INC COUNT_CUT
	RET
CLEAR_CUT	ENDP

