EXTRN	HERO_LEVEL:WORD		,HERO_MAX_HP:WORD	,HERO_HP:WORD
EXTRN	HERO_ATK:WORD		,HERO_DEF:WORD		,HERO_AGI:WORD
EXTRN	HERO_LIMIT:WORD
PUBLIC	LEVELUP
.MODEL	SMALL
.CODE
LEVELUP PROC	
	ADD	 HERO_LEVEL,1
	ADD	 HERO_MAX_HP,10
	MOV	 AX,HERO_MAX_HP
	MOV	 HERO_HP,AX
	ADD	 HERO_ATK,3
	ADD	 HERO_DEF,2
	ADD	 HERO_AGI,1	
	ADD	 HERO_LIMIT,10
	RET
LEVELUP ENDP
END