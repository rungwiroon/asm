EXTRN	LEVELUP:NEAR
.MODEL SMALL
.386
INCLUDE MACRO.ASM
.STACK 100H
.DATA
INCLUDE VARIABLE.ASM
.CODE
MAIN PROC
;STARTUP
	MOV	AX,@DATA
	MOV	DS,AX
	MOV	ES,AX
;SAVE DISPLAY MODE IF NECESSORY
	MOV	AH,0FH
	INT		10H
	PUSH	AX
;CHANGE DISPLAY MODE  TO 13H(320*200:256 COLORS)
	MOV	AX,0013H	
	INT		10H	
			
	
	
	CALL	MAIN_MENU 	
;LOAD OLD DISPLAY MODE1
	POP		AX
	MOV	AH,00H
	INT		10H
;DOS EXIT
	MOV	AH,4CH
	INT		21H
;EXIT
MAIN	ENDP
