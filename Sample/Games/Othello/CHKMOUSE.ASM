
CHECK_FIRST_PAGE	PROC

CHECK_PLAY :

	CMP	X_MICKY,42
	JGE	CHECK_X_PLAY
	JMP	CHECK_HOWTO
CHECK_X_PLAY :
	CMP	X_MICKY,240
	JLE	CHECK_Y_PLAY
	JMP	CHECK_HOWTO
CHECK_Y_PLAY :
	CMP	Y_MICKY,47
	JGE	CHECK_SCOPE_PLAY
	JMP	CHECK_HOWTO
CHECK_SCOPE_PLAY :
	CMP	Y_MICKY,70
	JLE	CLICK_PLAY
	JMP	CHECK_HOWTO
CLICK_PLAY :

	MOV	CHECK_CLICK,1
;	CALL	CHECK_SELECT_PAGE
	RET
;------------------------------------------------------
CHECK_HOWTO :

	CMP	X_MICKY,42
	JGE	CHECK_X_HOWTO
	JMP	CHECK_TOPSCORE
CHECK_X_HOWTO :
	CMP	X_MICKY,240
	JLE	CHECK_Y_HOWTO
	JMP	CHECK_TOPSCORE
CHECK_Y_HOWTO :
	CMP	Y_MICKY,74
	JGE	CHECK_SCOPE_HOWTO
	JMP	CHECK_TOPSCORE
CHECK_SCOPE_HOWTO :
	CMP	Y_MICKY,98
	JLE	CLICK_HOWTO
	JMP	CHECK_TOPSCORE
CLICK_HOWTO :
	
	HIDE_MOUSE

	PIC_LOCATE 0,0,PIC_HOWTO

	SHOW_MOUSE

	WAITCLICK

	HIDE_MOUSE
	
	PIC_LOCATE 0,0,PIC_BG
	MOV	CHECK_CLICK,0
	RET
;---------------------------------------------------------
CHECK_TOPSCORE :

	CMP	X_MICKY,42
	JGE	CHECK_X_TOPSCORE
	JMP	CHECK_QUIT
CHECK_X_TOPSCORE :
	CMP	X_MICKY,240
	JLE	CHECK_Y_TOPSCORE
	JMP	CHECK_QUIT
CHECK_Y_TOPSCORE :
	CMP	Y_MICKY,98
	JGE	CHECK_SCOPE_TOPSCORE
	JMP	CHECK_QUIT
CHECK_SCOPE_TOPSCORE :
	CMP	Y_MICKY,122
	JLE	CLICK_TOPSCORE
	JMP	CHECK_QUIT
CLICK_TOPSCORE :
	
	HIDE_MOUSE

	PIC_LOCATE 0,0,PIC_TOPSCORE

;	CALL	CHECK_FILE      ;\
;	CALL	GET_FILE	; \	
;	CALL	SORT_DATA	;  > in SAVELOAD.ASM
	CALL	ReadFromFile
	CALL	SHOW_FILE	;/

	SHOW_MOUSE

	WAITCLICK

	HIDE_MOUSE
	PIC_LOCATE 0,0,PIC_BG
	MOV	CHECK_CLICK,0
	RET
;---------------------------------------------------------
CHECK_QUIT :

	CMP	X_MICKY,42
	JGE	CHECK_X_QUIT
	JMP	NOT_ALL
CHECK_X_QUIT :
	CMP	X_MICKY,240
	JLE	CHECK_Y_QUIT
	JMP	NOT_ALL
CHECK_Y_QUIT :
	CMP	Y_MICKY,125
	JGE	CHECK_SCOPE_QUIT
	JMP	NOT_ALL
CHECK_SCOPE_QUIT :
	CMP	Y_MICKY,149
	JLE	CLICK_QUIT
	JMP	NOT_ALL
CLICK_QUIT :
;dos exit
        MOV     AX,0003H
        INT     10H

        MOV     AH,4CH
        INT     21H
;------------------------------------------------------------
NOT_ALL :
	SHOW_MOUSE
	MOV	CHECK_CLICK,0
	RET

CHECK_FIRST_PAGE	ENDP
