FIND_ROW	PROC

	MOV	COUNT,1
	MOV	TEMP,27

CHECK_TEMP2 :
	ADD	TEMP,2
	MOV	AX,TEMP
	CMP	Y_MICKY,AX
	JGE	CHECK_SCOPE2

	MOV	ROW,9
	RET

CHECK_SCOPE2 :

	ADD	TEMP,16
	MOV	AX,TEMP

	CMP	Y_MICKY,AX
	JLE	ROW_SUCCESS

	INC	COUNT
	CMP	COUNT,8
	JLE	CHECK_TEMP2

	MOV	ROW,9
	RET

ROW_SUCCESS :

	MOVM	ROW,COUNT
	RET

FIND_ROW	ENDP
