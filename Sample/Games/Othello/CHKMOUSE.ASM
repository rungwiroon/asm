FIND_COL	PROC

	MOV	COUNT,1
	MOV	TEMP,142

CHECK_TEMP1 :
	ADD	TEMP,4
	MOV	AX,TEMP
	CMP	X_MICKY,AX
	JGE	CHECK_SCOPE1

	MOV	COL,9
	RET

CHECK_SCOPE1 :

	ADD	TEMP,40
	MOV	AX,TEMP

	CMP	X_MICKY,AX
	JLE	COL_SUCCESS

	INC	COUNT
	CMP	COUNT,8
	JLE	CHECK_TEMP1

	MOV	COL,9
	RET

COL_SUCCESS :

	MOVM	COL,COUNT

	RET

FIND_COL	ENDP
