PUT_PIC_SHELL	PROC	NEAR

	PUSH	BX
	CALL	TO_PIXEL	;return x_pixel and y_pixel

	XOR	BX,BX
	MOV	BL,POSITION
	CMP	[TABLE+BX],0
	JE	_BLANK
	CMP	[TABLE+BX],1
	JE	_WHITE
	CMP	[TABLE+BX],2
	JE	_BLACK

_BLANK :
	PIC_DELAY	X_PIXEL,Y_PIXEL,BLANK
	JMP PUT_EXIT	
_WHITE :
	PIC_DELAY	X_PIXEL,Y_PIXEL,WHITE
	JMP PUT_EXIT
_BLACK :
	PIC_DELAY	X_PIXEL,Y_PIXEL,BLACK

PUT_EXIT :

	POP	BX
	RET

PUT_PIC_SHELL	ENDP