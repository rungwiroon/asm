PUSHX	MACRO	REGS
	IRP	R,<REGS>
	PUSH	R
	ENDM
ENDM

POPX	MACRO	REGS
	IRP	R,<REGS>
	POP	R
	ENDM
ENDM

GOTOXY	MACRO	gtX,gtY
	LOCAL	gtX,gtY
	pushx	<AX,DX,BX>
	MOV	AH,2
	MOV	DL,gtX
	MOV	DH,gtY
	MOV	BH,0
	INT	10H
	popx	<BX,DX,AX>
ENDM

PRINTDEC  MACRO	DECIMAL
	LOCAL	DECIMAL
	push	AX
	xor	ax,ax
	MOV	AX,DECIMAL
	CALL	OUTDEC
	pop	AX
ENDM
