LISTSCORE	PROC	NEAR
	LEA	SI,BMP_SCORE	;POINT TO SCORE BACKGROUND PIC
	MOV	BMP_X,0		;SET POS X
	MOV	BMP_Y,0		;SET POS Y
	CALL	LOADBMP		;LOAD BMP

	LEA DX,F_SCORE		;LOAD SCORE
	MOV AX,3D00H
	INT 21H			;OPEN FILE
;	;JC  ERROR_BMP_1	;########
	MOV HANDLE,AX		;save handle

	MOV	CX,10		;SET FOR LOOP 10 TIMES
	MOV	SCR_Y,3		;SET FOR Y-AXIS

LP_DSPL:
	PUSH	CX		;KEEP LOOP COUNTER

	;READ DATA TO BUFFER
		MOV	BX,HANDLE
		MOV	AH,3FH
		MOV	CX,18
		LEA	DX,BUFFER
		INT	21H

	;MOVE CURSOR
		MOV	DH,SCR_Y	;Y AXIS
		MOV	DL,5		;X AXIS
		MOV	BH,00H
		MOV	AH,02H
		INT	10H

	;DISPLAY NAME
		MOV	AH,09H
		LEA	DX,BUFFER
		INT	21H

	;MOVE CURSOR
		MOV	DH,SCR_Y	;Y AXIS
		MOV	DL,30		;X AXIS
		MOV	BH,00H
		MOV	AH,02H
		INT	10H

	;DISPLAY SCORE
		MOV	AH,09H
		LEA	DX,BUFFER
		ADD	DX,11
		INT	21H

		POP	CX		;RESTORE COUNTER
		ADD	SCR_Y,2		;COUNT UP Y-AXIS

LOOP	LP_DSPL

;CLOSE FILE
	MOV	AH,3EH
	MOV	BX,HANDLE
	INT	21H

	RET
LISTSCORE	ENDP
