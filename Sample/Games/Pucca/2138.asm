AINDEC PROC
;reads a number in range -32768 to 32767 FROM FILE
;input: FILE [SI]
;output: AX = binary equivalent of number
	PUSH BX		;save registers used
	PUSH CX
	PUSH DX
	PUSH SI
	PUSH DI
;print prompt
@ABEGIN:
;total = 0
	 XOR BX,BX	;BX hold total
;negative = false	 
	 XOR CX,CX	;CX hold sign
	 MOV AL,[SI]
;case charecter of	 
	 CMP AL,'-'
	 JE @AMINUS
	 CMP AL,'+'
	 JE @APLUS
	 JMP @AREPEAT2
@AMINUS:
	 MOV CX,1
@APLUS:
	 INC SI
	 MOV AL,[SI]
;end case
@AREPEAT2:
;if charecter is between '0' and '9'
	CMP AL,'0'
	JNGE @AEXIT ;@ANOT_DIGIT
	CMP AL,'9'
	JNLE @AEXIT ;@ANOT_DIGIT
;then convert charecter to a digit	
	AND AX,000FH
	PUSH AX
;total = total * 10 + digit
	MOV AX,10
	MUL BX
	POP BX
	ADD BX,AX
;read a charecter	
 	 ;INC SI
	 INC SI
	 MOV AL,[SI]
	CMP AL,' '
	JNE @AREPEAT2

;until CR
	MOV AX,BX
@AEXIT2:
;if negative
	OR CX,CX
	JE @AEXIT
;then
	NEG AX
;end if	
@AEXIT:
	POP DI
	POP SI
	POP DX
	POP CX
	POP BX
	RET
;here if illegal charecter entered	
@ANOT_DIGIT:
	CMP AL,' '
	JNE @AREPEAT2
	JE @AEXIT2
	JMP @ABEGIN
AINDEC ENDP
